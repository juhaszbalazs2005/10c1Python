"""Írj programot, ami beolvas két számot, majd kiírja:
a. az összegüket;
b. különbségüket;
c. szorzatukat;
d. hányadosukat, ha lehet!
e. mennyi maradékot kapunk, ha az első bekért számot osztjuk a másikkal!"""
a = int(input("Kérek egy számot! a="))
b = int(input("Kérek még egy számot! b="))
print("A két szám összege: ", a + b)
print("A két szám külömbsége: ", a - b)
print("A két szám szorzata: ", a * b)
print("A két szám hányadosa: ", a // b)
print("A két szám osztva: ", a / b)